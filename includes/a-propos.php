        <h1>À propos</h1>
        <p>Les Gaïennes est une agence parisienne et pluridiciplinaire composée de 7 jeunes femmes de 3 pôles
            distincts : le pôle création,
            le pôle communication et le pôle développement. Diversité et complémentarité,
            l’esprit d’équipe et la solidarité nous permettent de mener à bien nos projets.
            C’est en souhaitant se positionner dans la société actuelle et s’engager notamment dans la lutte
            pour l’environnement que nous vient l’idée du projet
            Veg & Co’.
            <br>
            Veg & Co’ a pour objectif, d’offrir à ses membres des fonctionnalités nécessaires dans l’accompagnement
            de leur mode de vie végétarien ou flexitarien.
        </p>
